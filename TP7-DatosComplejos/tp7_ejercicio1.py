# Objetivo: Partiendo de un diccionario de precios de frutas,
#           agregar nuevas frutas con sus respectivos precios.
#           (Actividad 1 del TP)

# Creamos el diccionario inicial con los precios de algunas frutas.
precios_frutas = {'Banana': 1200, 'Ananá': 2500, 'Melón': 3000, 'Uva': 1450}

# Agregamos las frutas pedidas con su precio correspondiente.
precios_frutas['Naranja'] = 1200   # Agregamos Naranja con precio 1200
precios_frutas['Manzana'] = 1500   # Agregamos Manzana con precio 1500
precios_frutas['Pera'] = 2300      # Agregamos Pera con precio 2300

# Mostramos el diccionario resultante por consola para verificar.
print('Diccionario de precios actualizado:', precios_frutas)
